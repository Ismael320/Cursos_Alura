/* 
 * 
 * transformar o código a seguir que utiliza um laço while em um código que utilize o for
 * 
 * 
 * package atividades1;
 * 
 * public class ProgramaFor {
 * 
 * public static void main(String args[]) { int contador = 0; while(contador <=
 * 10) { System.out.println(contador); contador++; } } }
 * 
 */

package atividades1;

public class ProgramaFor {

	public static void main(String args[]) {
		for (int contador = 0; contador <= 10; contador++) {
			System.out.println(contador);

		}

	}
}
